    public TypeChecker() {
        this.typeTab = new float[18][18];
        initComponents();
        try {
            BufferedReader file = new BufferedReader(new FileReader("src/Res/type.txt"));
            String line;
            int j = 0;
            while ((line = file.readLine()) != null) {
                String[] tab = line.split(";");
                for (int i = 0; i < tab.length; i++) {
                    typeTab[i][j] = Float.parseFloat(tab[i]);
                }
                j++;
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

