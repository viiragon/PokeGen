    private void changeType(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeType
        float sum = 1;
        String att = AttackType.getSelectedItem().toString();
        sum *= typeEffect(att, FirstType.getSelectedItem().toString());
        sum *= typeEffect(att, SecondType.getSelectedItem().toString());
        sum *= typeEffect(att, ThirdType.getSelectedItem().toString());
        if (sum >= 1 || sum == 0) {
            Summary.setText("x" + ((int) sum));
        } else {
            Summary.setText("x" + sum);
        }
        research();
        this.repaint();
    }//GEN-LAST:event_changeType

