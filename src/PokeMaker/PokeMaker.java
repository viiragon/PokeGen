    private class StatMeter extends javax.swing.JPanel {

        public int length, start;

        @Override
        public void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D g2d = (Graphics2D) g;

            for (int i = 1; i <= 6; i++) {
                javax.swing.JSpinner spin = HPSp;

                length = (TypeCCB.getWidth() + TypeCCB.getX()) - (HPSp.getX() + HPSp.getWidth()) - 5;
                start = HPSp.getX() + HPSp.getWidth() + 5;

                switch (i) {
                    case 1:
                        spin = HPSp;
                        break;
                    case 2:
                        spin = AtkSp;
                        break;
                    case 3:
                        spin = DefSp;
                        break;
                    case 4:
                        spin = SpaSp;
                        break;
                    case 5:
                        spin = SpdSp;
                        break;
                    case 6:
                        spin = SpeSp;
                        break;
                }

                Color hue = Color.getHSBColor((float) spin.getValue().hashCode() / 255 * 5 / 7, 1f, 1f);

                g2d.setColor(hue);
                g2d.fill(new Rectangle(start, spin.getY(),
                        (int) ((float) length * spin.getValue().hashCode() / 255) + 2, 22));
                g2d.setColor(Color.BLACK);
                g2d.drawRect(start, spin.getY(),
                        (int) ((float) length * spin.getValue().hashCode() / 255) + 1, 21);
            }
        }
    }

