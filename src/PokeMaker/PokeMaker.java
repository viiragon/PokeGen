    private void stateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_stateChanged
        javax.swing.JSpinner me = (javax.swing.JSpinner) evt.getSource();

        if (me.getValue().hashCode() > 255) {
            me.setValue(255);
        }
        if (me.getValue().hashCode() < 0) {
            me.setValue(0);
        }
        this.repaint();
    }//GEN-LAST:event_stateChanged

