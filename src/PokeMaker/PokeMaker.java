    public void setPokemon(PokemonBase poke) {
        if (question("Are you sure?")) {
            NumTF.setText("" + poke.getIndex());
            NameTF.setText(poke.getName());
            PokeLabelTF.setText(NameTF.getText());
            SpeciesTF.setText(poke.getSpecies());
            TypeACB.setSelectedItem(poke.getType(1));
            TypeBCB.setSelectedItem(poke.getType(2));
            TypeCCB.setSelectedItem(poke.getType(3));
            HPSp.setValue(poke.getHP());
            AtkSp.setValue(poke.getAtk());
            DefSp.setValue(poke.getDef());
            SpaSp.setValue(poke.getSpA());
            SpdSp.setValue(poke.getSpD());
            SpeSp.setValue(poke.getSpe());
            AbilityACB.setSelectedItem(poke.getAbility(1));
            AbilityBCB.setSelectedItem(poke.getAbility(2));
            AbilityHCB.setSelectedItem(poke.getAbility(3));
            EvolveCB.setSelectedItem(poke.getPreevolution());
            ConditionCB.setSelectedItem(poke.getCondition());
            EvolveLevelCB.setValue(poke.getEvolveLevel());
            EvolveThingCB.setSelectedItem(poke.getEvolveThing());
            BaseExpSp.setValue(poke.getBaseExp());
            HPEVSp.setValue(poke.getEvs(1));
            AtkEVSp.setValue(poke.getEvs(2));
            DefEVSp.setValue(poke.getEvs(3));
            SpaEVSp.setValue(poke.getEvs(4));
            SpdEVSp.setValue(poke.getEvs(5));
            SpeEVSp.setValue(poke.getEvs(6));
            CptRtSp.setValue(poke.getCaptureRate());
            GrwRtCB.setSelectedItem(poke.getGrowthRate());
            if (poke.getGender() == -1) {
                GendLessCB.setSelected(true);
            } else {
                GendLessCB.setSelected(false);
                MaleChanceSp.setValue(poke.getGender());
            }
            EggNmbSp.setValue(poke.getEggNumber());
            EggGrACB.setSelectedItem(poke.getEggGroup(1));
            EggGrBCB.setSelectedItem(poke.getEggGroup(2));
            ItemCB.setSelectedItem(poke.getItem());
            ItemPrctSp.setValue(poke.getItemChance());
            if (poke.getAlternativeForm() == null) {
                AltFormCB.setSelected(false);
                PokemonFormCB.setSelectedItem(poke);
                PokemonFormCB.setEnabled(false);
            } else {
                AltFormCB.setSelected(true);
                PokemonFormCB.setSelectedItem(poke.getAlternativeForm());
                PokemonFormCB.setEnabled(true);
            }
            DescrTA.setText(poke.getDescription(1));
            DescrTwoTA.setText(poke.getDescription(2));
        }
    }

